/* LOOPS = 
1. While loop = The while loop loops through a block of code as long as a specified condition is true:

Syntax :
while (condition) {
  // code block to be executed
}
*/


// Print 1 to 10 using while loop.
#include <iostream>
using namespace std;
int main() {
    int i= 1;
    while (i <= 100){
        cout << i << endl;
        i++;
         
    }
}